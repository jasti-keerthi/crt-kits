x=10
y=3
print(f"Addition                of x & y is {x+y}")
print(f"Substraction            of x & y    {x-y}")
print(f"Multiplication          of x & y    {x*y}")
print(f"Division                of x & y    {x/y}")
print(f"Modulo Division         of x & y    {x%y}")
print(f"Positive Floor Division of x & y   {x//y}")
print(f"Negative Floor Division of x & y  {-x//y}")
print(f"Exponential             of x & y   {x**y}")